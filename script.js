// Function to shuffle the pile of 30 cards and distribute

function shuffleCards() {
    let totalCards  = cards.length;
    let player1TotalCards = 0;
    let cpuTotalCards = 0;

    while(--totalCards >= 0) {
        let cardIdx = Math.floor(Math.random() * (totalCards +1));
        let randomisedCard = cards.splice(cardIdx, 1);

            if(player1TotalCards == cpuTotalCards) {
                player1Cards.push(randomisedCard[0]);
                player1TotalCards +=1;
                cpuScoreToBegin +=1;
            } else if(player1TotalCards > cpuTotalCards) {
                cpuCards.push(randomisedCard[0]);
                cpuTotalCards +=1;
                player1ScoreToBegin +=1;
            }
    }
}
