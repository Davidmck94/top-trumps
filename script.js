// If either player reaches a card count of zero, produce following message.

function gameOutcome(){
    if (cpuCards.length == 0 ){
        msgDisplay.textContent = "Well Done! You caught them all!"
    } else if (player1Cards.length == 0)
        msgDisplay.textContent = "Better luck next time!"
}

