//Through each round, a comparison of attributes takes place and an outcome is determined.

function processResult(outcome){

    if(outcome == 'player1-Wins'){
        
        player1Cards.push(player1Current)
        player1Cards.push(cpuCurrent)
        player1ScoreToBegin = player1Cards.length;
        cpuScoreToBegin = cpuCards.length;
        player1LiveScore.innerText = player1ScoreToBegin
        cpuLiveScore.innerText = cpuScoreToBegin

        if (player1Cards.length && cpuCards.length >= 1){
            distributeCards()
            player1Attributes()
            cpuAttributes()
        } else {
            gameOutcome()
        }
    }

     if (outcome == 'cpu-Wins'){
        
        cpuCards.push(player1Current)
        cpuCards.push(cpuCurrent)
        player1ScoreToBegin = player1Cards.length;
        cpuScoreToBegin= cpuCards.length;
        player1LiveScore.innerText = player1ScoreToBegin
        cpuLiveScore.innerText = cpuScoreToBegin

        if (player1Cards.length && cpuCards.length >= 1){
            distributeCards()
            player1Attributes()
            cpuAttributes()
        } else {
            gameOutcome()
        }
    }
}

